    }

    // ── aggregate score ──────────────────────────────────────────────────────

    [Fact]
    public void ManualTranscript_AddsUpToFivePointBonus()
    {
        var claims = new[] { Claim("c1", 3) };
        var facts = new[] { Fact("c1", Verdict.Supported) };

        var manual = _engine.Calculate(claims, facts, ContentDomain.General, TranscriptQuality.Manual);
        var auto = _engine.Calculate(claims, facts, ContentDomain.General, TranscriptQuality.AutoGenerated);

        // Manual bonus = 0.05 * 100 = 5 points
        Assert.Equal(5.0, manual.AggregateScore - auto.AggregateScore, precision: 5);
    }

    [Fact]
    public void AllScoresHundred_AggregateIsHundred()
    {
        var claims = new[] { Claim("c1", 3) };
        var facts = new[] { Fact("c1", Verdict.Supported, AccessibleSource()) };

        var result = _engine.Calculate(claims, facts, ContentDomain.General, TranscriptQuality.Manual);

        Assert.Equal(100.0, result.AggregateScore, precision: 5);
    }

    [Fact]
    public void ScoresNeverExceedHundred()
    {
        var claims = new[] { Claim("c1", 5) };
        var facts = new[] { Fact("c1", Verdict.Supported, AccessibleSource()) };

        var result = _engine.Calculate(claims, facts, ContentDomain.General, TranscriptQuality.Manual);

        Assert.True(result.AccuracyScore <= 100);
        Assert.True(result.SourceQualityScore <= 100);
        Assert.True(result.VerifiabilityScore <= 100);
        Assert.True(result.AggregateScore <= 100);
    }

    [Fact]
    public void ScoresNeverGoBelowZero()
    {
        var claims = new[] { Claim("c1", 5), Claim("c2", 5) };
        var facts = new[] { Fact("c1", Verdict.Refuted), Fact("c2", Verdict.Refuted) };

        var result = _engine.Calculate(claims, facts, ContentDomain.General, TranscriptQuality.AutoGenerated);

        Assert.True(result.AccuracyScore >= 0);
        Assert.True(result.AggregateScore >= 0);
    }

    // ── edge cases ───────────────────────────────────────────────────────────

    [Fact]
    public void NoClaimsAtAll_AllScoresZero()
    {
        var result = _engine.Calculate([], [], ContentDomain.General, TranscriptQuality.AutoGenerated);

        Assert.Equal(0.0, result.AccuracyScore);
        Assert.Equal(0.0, result.SourceQualityScore);
        Assert.Equal(0.0, result.VerifiabilityScore);
    }

    [Fact]
    public void SingleClaim_ScoresCorrectly()
    {
        var claims = new[] { Claim("c1", 3) };
        var facts = new[] { Fact("c1", Verdict.Supported, AccessibleSource()) };

        var result = _engine.Calculate(claims, facts, ContentDomain.Health, TranscriptQuality.AutoGenerated);

        Assert.Equal(100.0, result.AccuracyScore);
        Assert.Equal(100.0, result.SourceQualityScore);
        Assert.Equal(100.0, result.VerifiabilityScore);
    }

    [Fact]
    public void ScoreMethod_IdentifiesAlgorithmVersion()
    {
        var result = _engine.Calculate([], [], ContentDomain.General, TranscriptQuality.AutoGenerated);

        Assert.Equal("v1.0-weighted", result.ScoreMethod);
    }

    [Fact]
    public void ClaimWithNoMatchingFactCheck_IsIgnored()
    {
        // Claim c2 has no fact-check — should not affect scoring
        var claims = new[] { Claim("c1", 3), Claim("c2", 3) };
        var facts = new[] { Fact("c1", Verdict.Supported) };

        var result = _engine.Calculate(claims, facts, ContentDomain.General, TranscriptQuality.AutoGenerated);

        Assert.Equal(100.0, result.AccuracyScore);
    }
}
