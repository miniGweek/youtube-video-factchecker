    public void HalfUnverifiable_VerifiabilityIsFifty()
    {
        var claims = new[] { Claim("c1", 3), Claim("c2", 3) };
        var facts = new[] { Fact("c1", Verdict.Supported), Fact("c2", Verdict.Unverifiable) };

